from src.helpers import system

is_windows = system()


def get_user_agent(wait):

    try:

        chrome_options = chrome_options = webdriver.ChromeOptions()

        chrome_options.add_argument("--window-size=1920,1080")

        driver = webdriver.Chrome(
            service=Service(ChromeDriverManager().install()), options=chrome_options
        )

        driver.minimize_window()

        driver.get("https://www.whatsmyua.info/")

        user_agent = (
            WebDriverWait(driver, wait)
            .until(
                expected_conditions.presence_of_element_located(
                    (By.XPATH, r"//*[@id='custom-ua-string']")
                )
            )
            .text
        )

    except:

        user_agent = get_user_agent_fixed()

    print(
        "\n\n"
        + colored(" ! ", "green", attrs=["reverse"]) * (is_windows)
        + emoji.emojize(":check_mark_button:") * (not is_windows)
        + colored(
            "  User Agent found",
            "green",
        )
    )

    return user_agent


