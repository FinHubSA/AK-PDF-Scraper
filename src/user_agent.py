def user_agent(system):

    try:

        chrome_options = chrome_options = webdriver.ChromeOptions()

        chrome_options.add_argument("--window-size=1920,1080")

        logging.getLogger("WDM").setLevel(logging.NOTSET)

        driver = webdriver.Chrome(
            service=Service(ChromeDriverManager().install()), options=chrome_options
        )

        driver.minimize_window()

        driver.get(
            "https://techblog.willshouse.com/2012/01/03/most-common-user-agents/"
        )

        time.sleep(20)

        user_agent = driver.find_element(By.XPATH, r"//*[@id='your-useragent']").text

    except:

        user_agent = user_agent_fixed(system)

    if system == "Windows":

        print(
            "\n\n"
            + colored(" ! ", "green", attrs=["reverse"])
            + colored(
                "   User Agent found",
                "green",
            )
        )
    else:

        print(
            "\n\n"
            + emoji.emojize(":check_mark_button:")
            + colored(
                "   User Agent found",
                "green",
            )
        )

    return user_agent


